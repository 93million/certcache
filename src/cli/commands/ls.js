const listCerts = require('../../lib/listCerts')

module.exports = {
  cmd: 'ls',
  desc: 'List certificates',
  builder: {
    extensions: {
      description: 'List certificates generated by comma separated list of extensions',
      examples: 'certbot,thirdparty'
    }
  },
  handler: (argv) => {
    listCerts(argv).catch((e) => {
      console.error(e)
      process.exit(1)
    })
  }
}
